# Определяем функцию square, которая принимает один аргумент - сторону квадрата

def square(side):
    # Вычисляем площадь квадрата как сторону в квадрате
    return side ** 2


# Пример использования функции
side_length = 5
area = square(side_length)
print(f"Площадь квадрата со стороной {side_length} равна {area}")
