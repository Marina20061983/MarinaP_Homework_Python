# Создаем функцию fizz_buzz, которая принимает один аргумент n
def fizz_buzz(n):
    # Используем цикл for для перебора всех чисел от 1 до n включительно
    for i in range(1, n + 1):
        # Проверяем, делится ли число на 3 и на 5
        if i % 3 == 0 and i % 5 == 0:
            print("FizzBuzz")
        # Проверяем, делится ли число только на 3
        elif i % 3 == 0:
            print("Fizz")
        # Проверяем, делится ли число только на 5
        elif i % 5 == 0:
            print("Buzz")
        else:
            # Если ни одно из условий не выполнено, просто печатаем число
            print(i)


# Пример вызова функции
fizz_buzz(15)
